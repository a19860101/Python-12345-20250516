# 運算元 operator

x = 10
# 算術運算元
print(x + 2)
print(x - 2)
print(x * 2)
print(x / 2)
print(x % 3) #求餘數
print(x ** 10)
print(x // 3)

# 比較運算元
print(x > 0)
print(x < 0)
print(x >= 0)
print(x <= 0)
print(x == 0)

# 指定運算元
x += 2 # x = x + 2
print(x) # 12
x -= 2
print(x) # 10
x *= 2
print(x) # 20
x /= 2
print(x) # 10
x %= 2
print(x) # 0
x **= 2
x //= 2